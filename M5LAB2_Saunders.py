import turtle

def main():
    # Write a program using turtle graphics that displays first and last initials.
    
    turtle.pencolor("red")
    turtle.pensize(10)
    turtle.penup()
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.pendown()
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.penup()
    turtle.forward(50)
    turtle.pendown()
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(25)
    turtle.left(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(25)
    turtle.right(90)
    turtle.forward(50)
    
      
main()
